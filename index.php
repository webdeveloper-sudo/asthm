            let currentIndex = 0;
            const totalSlides = slides.length;
            const intervalTime = 6000; // 6 seconds per slide
            let slideInterval;

            function showSlide(index) {
                // Remove active class from current active slide and dot
                slides[currentIndex].classList.remove('active');
                dots[currentIndex].classList.remove('active');

                // Set new index
                currentIndex = index;

                // Add active class to new active slide and dot
                slides[currentIndex].classList.add('active');
                dots[currentIndex].classList.add('active');
            }

            function nextSlide() {
                const nextIndex = (currentIndex + 1) % totalSlides;
                showSlide(nextIndex);
            }

            function startSlideShow() {
                stopSlideShow();
                slideInterval = setInterval(nextSlide, intervalTime);
            }

            function stopSlideShow() {
                if (slideInterval) {
                    clearInterval(slideInterval);
                }
            }

            // Click interaction for navigation dots
            dots.forEach((dot, index) => {
                dot.addEventListener('click', () => {
                    showSlide(index);
                    startSlideShow(); // restart interval
                });
            });

            // Start the slide show
            startSlideShow();
        });
